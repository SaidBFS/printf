char	*ft_strrchr(const char *str, int c)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	while (str[i] != c && i > 0)
		i--;
	if (str[0] != c && i == 0)
		return (0);
	return ((char*)&str[i]);
}
