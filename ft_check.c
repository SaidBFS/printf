#include "libftprintf.h"

int     ft_check_conv(char *str, int i)
{
    i++;
    while (str[i])
    {
        if (str[i] == 'c' || str[i] == 's' || str[i] == 'p' || str[i] == 'd'
            || str[i] == 'i' || str[i] == 'u' || str[i] == 'x' || str[i] == 'X' 
            || str[i] == '%')
            return (1);
        i++;
    }
    return (0);
}
